export type AcademicCalendarType = "trimester" | "semester";

export const getPeriodLabel = (
  period: string,
  language: "fr" | "en",
  calendarType: AcademicCalendarType = "trimester"
) => {
  const normalized = String(period || "").toLowerCase();

  if (language === "fr") {
    if (normalized === "term1") return calendarType === "semester" ? "Semestre 1" : "Trimestre 1";
    if (normalized === "term2") return calendarType === "semester" ? "Semestre 2" : "Trimestre 2";
    if (normalized === "term3") return calendarType === "semester" ? "Semestre 3" : "Trimestre 3";
    if (normalized === "annual") return "Annuel";
    return period;
  }

  if (normalized === "term1") return calendarType === "semester" ? "Semester 1" : "Term 1";
  if (normalized === "term2") return calendarType === "semester" ? "Semester 2" : "Term 2";
  if (normalized === "term3") return calendarType === "semester" ? "Semester 3" : "Term 3";
  if (normalized === "annual") return "Annual";
  return period;
};

/**
 * Get a translation string
 * @param key - Translation key (e.g., "dashboard.title")
 * @param language - Language code ("fr" or "en")
 * @param vars - Optional variables to interpolate (e.g., { name: "John" })
 * @returns Translated string
 */
export function t(
  key: string,
  language: "fr" | "en",
  vars?: Record<string, string>
): string {
  let text = (translations[language] as Record<string, string>)?.[key] || `[MISSING: ${key}]`;

  if (vars) {
    Object.entries(vars).forEach(([varName, value]) => {
      text = text.replace(`{${varName}}`, value);
    });
  }

  return text;
}

/**
 * Get all translations for a specific language
 * @param language - Language code ("fr" or "en")
 * @returns Full translation object
 */
export function getLanguageDict(language: "fr" | "en") {
  return translations[language];
}
